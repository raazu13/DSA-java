public class Q2_NumericLiterals {
    public static void main(String[] args) {

        int binary=0b1001;

        int octal=012;
        int hex=0xA;

        System.out.println("Binary literal 1001 is : "+binary);
        System.out.println("octal literal 12 is : "+octal);
        System.out.println("hex literal A is :"+hex);
    }
}
