public class loops {
    public static void main(String[] args){



        for(int i =0; i<=10; i++){   //it is an example of for loop
            System.out.println(i);

        }


        // let's start while loop
        int num= 1;

        while(num < 11){
            System.out.print(num+" ");

            num += 1;

        }

        // do while loop
        int i=0;
        do{
            System.out.println(i);
            i +=1;
        }while(i<11);



    }

}
